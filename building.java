// Class building
public class building {

	// Building number
	int buildingNum;

	// Total number of days spent so far on the building
	int executed_time;

	// Total number of days required to complete the construction
	int total_time;
	building(int buildingNum,int executed_time,int total_time){
		this.buildingNum=buildingNum;
		this.executed_time=executed_time;
		this.total_time=total_time;
		
	}
}
    
